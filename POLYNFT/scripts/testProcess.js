const polling = async (timeout, interval, Txhash) => {
  return new Promise(async (resolve, reject) => {
    let startTime = (new Date()).getTime();
    interval = interval || 1000;
    let canPoll = true;
    async function poll() {
      canPoll = ((new Date()).getTime() - startTime) <= timeout;
      let date = await provider.getTransactionReceipt(Txhash);
      // console.log("ðŸš€ ~ file: testProcess.js:579 ~ poll ~ date:", date)
      if(date && date.blockNumber){
        canPoll = false;
        resolve(date)
      }
      if (canPoll) {
        setTimeout(poll, interval);
      }
    }
     poll();
  })
}

